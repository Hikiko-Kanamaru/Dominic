	public static NumbersDomain getNumbersDomain(int number) {
		NameListDomain NameLD = new NameListDomain(number, DefaultList.NAME[number]);
		ActionListDomain ActionLD = new ActionListDomain(number,DefaultList.ACTION[number]);

		return  new NumbersDomain(99, number ,NameLD,ActionLD, 0);
	}


	public static NumberAndHuman getNumberAndHuman(int number) {
		return new NumberAndHuman(number, "human",
				DefaultList.NAME[number], number, "",
				DefaultList.ACTION[number],
				number, "");
	}

